class FusionLayer(nn.Module):
    def __init__(self, d_model, drop_rate, d_state, num_fusion_layer) -> None:
        super().__init__()
        self.layers = nn.ModuleList(
            [FusionBlock(
                d_model=d_model,
                dropout=drop_rate,
                d_state=d_state
            ) for _ in range(num_fusion_layer)]
        )
    
    def forward(self, x, y):
        for layer in self.layers:
            x_, y_ = layer(x, y)
            x, y = x + x_, y + y_
        return x + y
